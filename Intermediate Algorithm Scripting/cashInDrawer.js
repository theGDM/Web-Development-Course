function checkCashRegister(price, cash, cid) {
    let conversionObj = {
        "PENNY": 0.01,
        "NICKEL": 0.05,
        "DIME": 0.1,
        "QUARTER": 0.25,
        "ONE": 1,
        "FIVE": 5,
        "TEN": 10,
        "TWENTY": 20,
        "ONE HUNDRED": 100
    };

    let cidInCoins = {};
    for (let c of cid) {
        if (c[1] != 0) {
            for (let curr in conversionObj) {
                if (curr === c[0]) {
                    let coinsOfCurrency = Math.ceil(c[1] / conversionObj[curr]);
                    cidInCoins[c[0]] = coinsOfCurrency;
                }
            }
        } else {
            cidInCoins[c[0]] = 0;
        }
    }
    return cidInCoins;
}

